#include "../includes/libftprintf.h"

void	ft_maj(char **rec)
{
	int		i;

	i = -1;
	while ((*rec)[++i])
	{
		if ((*rec)[i] > 96 && (*rec)[i] < 123)
			(*rec)[i] -= 32;
	}
}
